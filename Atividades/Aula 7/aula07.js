/* && -> and -> e 
let a = true;
let b = false;
console.log(a && b); // Retorna false, porque a é verdadeiro mas b é falso
console.log(a && true); // Retorna true, porque ambos a e true são verdadeiros
*/


/* || -> or -> ou
let a = true;
let b = false;
console.log(a || b); // Retorna true, porque a é verdadeiro, mesmo que b seja falso
console.log(false || false); // Retorna false, porque ambos os lados são falsos
*/

/* !() -> not -> não
let a = true;
console.log(!a); // Retorna false, porque a é verdadeiro e a negação é falsa
let b = false;
console.log(!b); // Retorna true, porque b é falso e a negação é verdadeira
*/